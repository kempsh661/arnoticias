// Función para obtener las últimas 6 noticias (para el carrusel)
export const getLatestNews = (count = 6) => {
  return allNewsData
    .sort((a, b) => new Date(b.date) - new Date(a.date))
    .slice(0, count)
}

// Función para obtener noticias destacadas (excluyendo las del carrusel)
export const getHighlightedNews = (count = 3) => {
  const latestNews = getLatestNews(6)
  const latestIds = latestNews.map(news => news.id)
  
  return allNewsData
    .filter(news => !latestIds.includes(news.id))
    .sort((a, b) => new Date(b.date) - new Date(a.date))
    .slice(0, count)
}
